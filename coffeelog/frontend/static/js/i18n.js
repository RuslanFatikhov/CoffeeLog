function normalizeLang(lang) {
  const normalized = String(lang || "").trim().toLowerCase();
  return SUPPORTED_LANGS.has(normalized) ? normalized : DEFAULT_LANG;
}

export function getLanguage() {
  const fromStorage = localStorage.getItem(LANG_KEY);
  if (fromStorage) return normalizeLang(fromStorage);
  const fromNavigator = String(navigator.language || "").slice(0, 2);
  return normalizeLang(fromNavigator);
}

export function setLanguage(lang) {
  const nextLang = normalizeLang(lang);
  localStorage.setItem(LANG_KEY, nextLang);
  document.documentElement.lang = nextLang;
  return nextLang;
}

export function t(key, fallback = "", params = {}) {
  const lang = getLanguage();
  const dict = DICTIONARY[lang] || DICTIONARY[DEFAULT_LANG];
  const base = dict?.[key] ?? DICTIONARY[DEFAULT_LANG]?.[key] ?? fallback ?? key;
  return String(base).replace(/\{(\w+)\}/g, (_, token) => String(params[token] ?? ""));
}

export function applyTranslations(root = document) {
  root.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.getAttribute("data-i18n");
    if (!key) return;
    node.textContent = t(key, node.textContent || "");
  });

  root.querySelectorAll("[data-i18n-placeholder]").forEach((node) => {
    const key = node.getAttribute("data-i18n-placeholder");
    if (!key) return;
    node.setAttribute("placeholder", t(key, node.getAttribute("placeholder") || ""));
  });

  root.querySelectorAll("[data-i18n-aria-label]").forEach((node) => {
    const key = node.getAttribute("data-i18n-aria-label");
    if (!key) return;
    node.setAttribute("aria-label", t(key, node.getAttribute("aria-label") || ""));
  });
}
